let a = 5;
let b = 2;

console.log("Arithmetic Operators");

console.log("a = ", a, " b = ", b);
console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b);
console.log("a % b = ", a % b); // Modulus
console.log("a ** b = ", a ** b); // power

//Unary Operators
console.log("a++ = ", a++); //post increment
console.log("a = ", a);

console.log("++ b", ++b); //pre increment
console.log("b = ", b);


